package git.sample;

public class Car {
	private int speed;
	private String color = "blue";

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public String getBodyColor() {
		return color;
	}

	public void setBodyColor(String bodyColor) {
		this.color = bodyColor;
	}
}
